CREATE OR REPLACE FUNCTION INSERIR(P_TABELA TEXT, VARIADIC P_VALORES TEXT[]) RETURNS VOID AS $$
DECLARE
    VALORES TEXT;
    F_SQL TEXT;
BEGIN 
    SELECT STRING_AGG(QUOTE_NULLABLE(VAL), ', ') INTO VALORES FROM UNNEST(P_VALORES) AS VAL;

	CASE LOWER(P_TABELA)
		WHEN 'categoria' THEN
			F_SQL := FORMAT('INSERT INTO CATEGORIA(NOME) VALUES (%s)', VALORES);
		WHEN 'produto' THEN 
			F_SQL := FORMAT('INSERT INTO PRODUTO(NOME, DESCRICAO, VALOR, ID_CAT, ID_MARCA) VALUES (%s)', VALORES);
		ELSE
			RAISE EXCEPTION 'Não existe nenhuma tabela com o nome %.', P_TABELA;
	END CASE;
	EXECUTE F_SQL;
	
	EXCEPTION
		WHEN SQLSTATE '22001' THEN
			RAISE EXCEPTION 'O nome fornecido excede a quantidade máxima de caracteres permitidos.';
		WHEN OTHERS THEN
			RAISE EXCEPTION 'Não foi possível fazer a inserção na tabela %. ERRO: %', P_TABELA, SQLERRM
			USING HINT = 'Cheque a ordem e a quantidade dos parâmetros.OBS.: IDs são serial.';
END;
$$ LANGUAGE PLPGSQL;


