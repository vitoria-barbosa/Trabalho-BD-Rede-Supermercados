CREATE OR REPLACE FUNCTION DELETAR(TABELA TEXT, DADOS JSONB) RETURNS VOID AS $$
DECLARE
	F_SQL TEXT;
	F_LINHAS TEXT;
	QTD_LINHAS INT;
	F_CONDICOES TEXT;
	CAMPO RECORD;	
BEGIN
	IF (SELECT COUNT(*) FROM JSONB_OBJECT_KEYS(DADOS)) = 0 THEN
		RAISE EXCEPTION 'NENHUM CRITÉRIO (CHAVE: COLUNA) FOI INFORMADO.';
	END IF;
	
	PERFORM VALIDAR_NULOS_E_VAZIOS(DADOS, TABELA);
	
	F_CONDICOES := '';
	FOR CAMPO IN SELECT * FROM JSONB_EACH(DADOS)
	LOOP
		PERFORM VALIDAR_CHAVE(CAMPO.KEY, TABELA);

		IF F_CONDICOES = '' THEN
			F_CONDICOES := FORMAT('%I = %L', LOWER(CAMPO.KEY), UPPER(CAMPO.VALUE #>> '{}'));
		ELSE	
			F_CONDICOES := F_CONDICOES || FORMAT(' AND %I = %L ', LOWER(CAMPO.KEY), UPPER(CAMPO.VALUE #>> '{}'));
		END IF;
	END LOOP;

	IF F_CONDICOES = '' THEN
		RAISE EXCEPTION 'NENHUMA CONDIÇÃO VÁLIDA FOI GERADA.';
	END IF;

	F_LINHAS := FORMAT('SELECT COUNT(*) FROM %I WHERE ATIVO = TRUE AND ', LOWER(TABELA)) || F_CONDICOES;
	EXECUTE F_LINHAS INTO QTD_LINHAS;
	IF QTD_LINHAS = 0 THEN
		RAISE EXCEPTION 'NÃO FOI ENCONTRADO NENHUM REGISTRO VÁLIDO COM AS INFORMAÇÕES PASSADAS.';
	END IF;

	F_SQL := FORMAT('UPDATE %I SET ativo = FALSE WHERE ', LOWER(TABELA)) || F_CONDICOES;
	EXECUTE F_SQL;
	
	RAISE NOTICE 'DELEÇÃO DA LINHA "%", DA TABELA "%", REALIZADA COM SUCESSO!', DADOS, TABELA;
END;
$$ LANGUAGE PLPGSQL;

