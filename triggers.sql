CREATE OR REPLACE FUNCTION VALIDAR_CATEGORIA() RETURNS TRIGGER AS $$
BEGIN
	PERFORM VALIDAR_NULOS_E_VAZIOS(NEW, TG_TABLE_NAME);
	PERFORM VALIDAR_VALOR_UNICO('categoria', 'nome', NEW.NOME);
	PERFORM PERMITIR_SOMENTE_CARAC_ALFA('NOME', NEW.NOME);

	RETURN NEW;
END;
$$ LANGUAGE PLPGSQL;


