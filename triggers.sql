CREATE OR REPLACE FUNCTION VALIDAR_PRODUTO() RETURNS TRIGGER AS $$
BEGIN
	PERFORM VALIDAR_NULOS_E_VAZIOS(NEW, TG_TABLE_NAME);
	IF (TG_OP = 'INSERT') THEN
		PERFORM VALIDAR_VALOR_UNICO('produto', 'nome', NEW.NOME);
		
	ELSEIF (TG_OP = 'UPDATE' AND OLD.NOME != NEW.NOME) THEN
		PERFORM VALIDAR_VALOR_UNICO('produto', 'nome', NEW.NOME);
	END IF;
	
	IF (NEW.VALOR < 0) THEN 
		RAISE EXCEPTION 'O VALOR DO PRODUTO NÃO PODE SER NEGATIVO.';
	END IF;

	IF (NEW.ID_CAT IS NOT NULL) THEN
		PERFORM VALIDAR_ID('categoria', 'id_cat', NEW.ID_CAT);
	END IF;
	
	IF (NEW.ID_MARCA IS NOT NULL) THEN
		PERFORM VALIDAR_ID('marca', 'id_marca', NEW.ID_MARCA);
	END IF;

	RETURN NEW;
END;
$$ LANGUAGE PLPGSQL;


