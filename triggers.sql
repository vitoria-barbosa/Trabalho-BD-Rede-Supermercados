CREATE OR REPLACE FUNCTION VALIDAR_ESTOQUE() RETURNS TRIGGER AS $$
DECLARE
    JA_EXISTE BOOLEAN;
BEGIN
	PERFORM VALIDAR_NULOS_E_VAZIOS(NEW, TG_TABLE_NAME);
	PERFORM VALIDAR_ID('mercado', 'id_mercado', NEW.ID_MERC);
	PERFORM VALIDAR_ID('produto', 'id_prod', NEW.ID_PROD);
	
	SELECT EXISTS (SELECT * FROM ESTOQUE WHERE ID_MERC = NEW.ID_MERC AND ID_PROD = NEW.ID_PROD) INTO JA_EXISTE;
    
	IF JA_EXISTE AND TG_OP = 'INSERT' THEN
        RAISE EXCEPTION 'JÁ EXISTE O PRODUTO DE ID % NO ESTOQUE DO SUPERMERCADO DE ID %.', NEW.ID_PROD, NEW.ID_MERC;
    END IF;

	IF (NEW.QTD_ESTOQUE < 0) THEN
		RAISE EXCEPTION 'A QUANTIDADE DO PRODUTO NÃO PODE SER NEGATIVA.';
	END IF;

	RETURN NEW;
END;
$$ LANGUAGE PLPGSQL;


