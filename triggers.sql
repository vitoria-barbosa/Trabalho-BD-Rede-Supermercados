CREATE OR REPLACE FUNCTION A_VALIDAR_ITEM_VENDA() RETURNS TRIGGER AS $$
DECLARE
	QTD_DISP INT;
	DELTA_QTD INT;
BEGIN
	NEW.VALOR_TOTAL_ITEM := CALCULAR_TOTAL_ITEM(NEW.ID_ESTOQUE, NEW.QTD_VENDIDA);
	
	PERFORM VALIDAR_NULOS_E_VAZIOS(NEW, TG_TABLE_NAME);
	PERFORM VALIDAR_ID('ESTOQUE', 'ID_ESTOQUE', NEW.ID_ESTOQUE);
	PERFORM VALIDAR_ID('VENDA', 'ID_VENDA', NEW.ID_VENDA);
	
	SELECT QTD_ESTOQUE INTO QTD_DISP FROM ESTOQUE WHERE ID_ESTOQUE = NEW.ID_ESTOQUE;

	IF TG_OP = 'UPDATE' THEN
		DELTA_QTD := NEW.QTD_VENDIDA - OLD.QTD_VENDIDA;
	ELSIF TG_OP = 'INSERT' THEN
		DELTA_QTD := NEW.QTD_VENDIDA;
	END IF;
	
	IF DELTA_QTD > 0 AND QTD_DISP < DELTA_QTD THEN
		RAISE EXCEPTION 'ESTOQUE INSUFICIENTE PARA A VENDA.';
	END IF;
		
	RETURN NEW;
END;
$$ LANGUAGE PLPGSQL;


CREATE OR REPLACE FUNCTION B_ATUALIZAR_QTD_ESTOQUE() RETURNS TRIGGER AS $$
BEGIN
	IF TG_OP = 'INSERT' THEN
		UPDATE ESTOQUE
		SET QTD_ESTOQUE = QTD_ESTOQUE - NEW.QTD_VENDIDA
		WHERE ID_ESTOQUE = NEW.ID_ESTOQUE;
	ELSIF TG_OP = 'DELETE' THEN
		UPDATE ESTOQUE
		SET QTD_ESTOQUE = QTD_ESTOQUE + OLD.QTD_VENDIDA
		WHERE ID_ESTOQUE = OLD.ID_ESTOQUE;

	ELSIF TG_OP = 'UPDATE' THEN
		UPDATE ESTOQUE
		SET QTD_ESTOQUE = QTD_ESTOQUE - (NEW.QTD_VENDIDA - OLD.QTD_VENDIDA)
		WHERE ID_ESTOQUE = NEW.ID_ESTOQUE;
	END IF;

	RETURN CASE 
	WHEN TG_OP = 'DELETE' THEN OLD
	ELSE NEW
	END;

END;
$$ LANGUAGE PLPGSQL;


CREATE OR REPLACE FUNCTION C_ATUALIZAR_VALOR_VENDA() RETURNS TRIGGER AS $$
DECLARE
	NOVO_VALOR NUMERIC(10,2);
	V_ID_VENDA INT;
BEGIN
	V_ID_VENDA := COALESCE(NEW.ID_VENDA, OLD.ID_VENDA);
	
	SELECT COALESCE(SUM(VALOR_TOTAL_ITEM), 0) INTO NOVO_VALOR FROM ITEM_VENDA WHERE ID_VENDA = V_ID_VENDA;

	UPDATE VENDA SET VALOR_TOTAL = NOVO_VALOR
	WHERE ID_VENDA = V_ID_VENDA;

	RETURN NEW;
END;
$$ LANGUAGE PLPGSQL;


